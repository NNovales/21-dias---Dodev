//CRIANDO VARIÁVEL
var opcao = prompt("Escolha uma opção de 1 a 5")
//CRIANDO SWITCHCASE
switch (opcao) {
    case "1":
        console.log("Você escolheu a opção 1")
        break;
    case "2":
        console.log("Você escolheu a opção 2")
        break;
    case "3":
        console.log("Você escolheu a opção 3")    
        break;
    default:
        console.log("Opção inválida")    
        break;

}